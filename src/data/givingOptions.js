export const CURRENCY_OPTIONS = [
  { code: 'USD', label: 'USD - US Dollar', minorUnit: 2 },
  { code: 'EUR', label: 'EUR - Euro', minorUnit: 2 },
  { code: 'GBP', label: 'GBP - British Pound', minorUnit: 2 },
  { code: 'UGX', label: 'UGX - Ugandan Shilling', minorUnit: 0 },
  { code: 'KES', label: 'KES - Kenyan Shilling', minorUnit: 2 },
  { code: 'NGN', label: 'NGN - Nigerian Naira', minorUnit: 2 },
  { code: 'CAD', label: 'CAD - Canadian Dollar', minorUnit: 2 },
  { code: 'AUD', label: 'AUD - Australian Dollar', minorUnit: 2 },
]

export function getCurrencyConfig(currencyCode) {
  return CURRENCY_OPTIONS.find(({ code }) => code === currencyCode.toUpperCase()) ?? null
}

export function formatDonationAmount(amount, currencyCode) {
  const currency = getCurrencyConfig(currencyCode)

  if (!currency) {
    return ''
  }

  const numericAmount = Number(amount)

  if (!Number.isFinite(numericAmount) || numericAmount <= 0) {
    return ''
  }

  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: currency.code,
    minimumFractionDigits: currency.minorUnit,
    maximumFractionDigits: currency.minorUnit,
  }).format(numericAmount)
}

export function toStripeUnitAmount(amount, currencyCode) {
  const currency = getCurrencyConfig(currencyCode)
  const numericAmount = Number(amount)

  if (!currency || !Number.isFinite(numericAmount) || numericAmount <= 0) {
    return null
  }

  return currency.minorUnit === 0
    ? Math.round(numericAmount)
    : Math.round(numericAmount * 100)
}
